/**
 * Represents a practical application or use case
 * Used for documenting real-world implementations and challenges
 */
export interface Application {
  /** The area or domain of application */
  area: string;
  /** Detailed description of how it's applied */
  description: string;
  /** Array of concrete examples */
  examples: string[];
  /** Array of potential challenges or limitations */
  challenges: string[];
}
